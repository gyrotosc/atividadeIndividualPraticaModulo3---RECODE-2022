package br.com.jajatur.model;

public class Contacta {

	private int codAtendente;
	private int codCliente;
	private int codAtendimento;
	private String assuntoAtendimento;
	private String statusAtendimento;
	public int getCodAtendente() {
		return codAtendente;
	}
	public void setCodAtendente(int codAtendente) {
		this.codAtendente = codAtendente;
	}
	public int getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(int codCliente) {
		this.codCliente = codCliente;
	}
	public int getCodAtendimento() {
		return codAtendimento;
	}
	public void setCodAtendimento(int codAtendimento) {
		this.codAtendimento = codAtendimento;
	}
	public String getAssuntoAtendimento() {
		return assuntoAtendimento;
	}
	public void setAssuntoAtendimento(String assuntoAtendimento) {
		this.assuntoAtendimento = assuntoAtendimento;
	}
	public String getStatusAtendimento() {
		return statusAtendimento;
	}
	public void setStatusAtendimento(String statusAtendimento) {
		this.statusAtendimento = statusAtendimento;
	}
	
	
	
}
